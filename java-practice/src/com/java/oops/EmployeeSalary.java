package com.java.oops;

public class EmployeeSalary {
	String name;
	int salary;

	public EmployeeSalary(String name, int salary) {
		super();
		this.name = name;
		this.salary = salary;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSalary() {
		return salary;
	}

}
